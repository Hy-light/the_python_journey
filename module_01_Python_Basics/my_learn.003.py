# Strings
# Strides in strings
letters = "ABCDEFGHIJK"
strided_letters = letters[0:4] #returns the first four lettters
print(strided_letters)

good = "GsoAo+d"
strided_good = good[::2] # returns the alternating characters starting at index 0 
print(strided_good)

word = "uppercase"
print(word.upper())
